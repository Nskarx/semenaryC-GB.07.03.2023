// Задача 4: Найдите произведение пар чисел в одномерном массиве.
// Парой считаем первый и последний элемент, второй и предпоследний
// и т.д. Результат запишите в новом массиве.
// [1 2 3 4 5] -> 5 8 3
// [6 7 3 6] -> 36 21

using System;

class Program {
    static void Main(string[] args) {
        int[] arr = { 1, 2, 3, 4, 5 };
        int n = arr.Length;
        int[] result = new int[(n + 1) / 2];

        for (int i = 0; i < n / 2; i++) {
            result[i] = arr[i] * arr[n - i - 1];
        }
        
        if (n % 2 == 1) {
            result[(n - 1) / 2] = arr[n / 2];
        }

        Console.WriteLine("Произведения пар чисел в массиве:");
        for (int i = 0; i < (n + 1) / 2; i++) {
            Console.Write($"{result[i]} ");
        }
    }
}
// В данном коде мы создаем исходный массив arr и новый массив result, 
// в котором будут храниться произведения пар чисел. 
// Затем мы проходим по исходному массиву и вычисляем произведение пар чисел (первый и последний элемент, второй и предпоследний и т.д.)
//  с помощью формулы arr[i] * arr[n - i - 1], где i - текущий индекс элемента в массиве, n - длина массива. 
//  Полученные произведения записываем в соответствующие элементы нового массива result.
//  Если длина исходного массива нечетная, то последний элемент массива result будет равен среднему элементу массива arr. 
//  Наконец, выводим на экран элементы нового массива result.
