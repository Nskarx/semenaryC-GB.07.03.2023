// Задача 3: Задайте одномерный массив из 123 случайных чисел.
// Найдите количество элементов массива, значения которых лежат в
// отрезке [10,99].
// Пример для массива из 5, а не 123 элементов. В своём решении сделайте для
// 123
// [5, 18, 123, 6, 2] -> 1
// [1, 2, 3, 6, 2] -> 0
// [10, 11, 12, 13, 14] -> 5

using System;

class Program {
    static void Main(string[] args) {
        int count = 123; // Количество элементов массива
        int a = 10; // Начало диапазона
        int b = 99; // Конец диапазона
        
        int[] arr = new int[count];
        Random random = new Random();
        
        // Заполнение массива случайными числами
        for (int i = 0; i < count; i++) {
            arr[i] = random.Next(1, 101); // Числа от 1 до 100
        }
        
        // Подсчет количества элементов в отрезке [a, b]
        int result = 0;
        for (int i = 0; i < count; i++) {
            if (arr[i] >= a && arr[i] <= b) {
                result++;
            }
        }
        
        Console.WriteLine($"Количество элементов массива, значения которых лежат в отрезке [{a}, {b}]: {result}");
    }
}

// В данном коде мы создаем массив arr из 123 случайных чисел, где каждое число находится в диапазоне от 1 до 100 (включительно). 
// Затем мы проходим по этому массиву, проверяя каждый элемент на принадлежность к заданному диапазону [a, b]. 
// Количество элементов, удовлетворяющих условию, 
// записываем в переменную result и выводим ее на экран с помощью метода Console.WriteLine().